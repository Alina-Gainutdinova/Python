side = int(input("Введите количество сторон геометрической фигуры: "))

if side == 3:
    print("Треугольник")
elif side == 4:    
    print("Четырехугольник")
elif side == 5:
    print("Пятиугольник")
elif side == 6:
    print("Шестиугольник") 
elif side == 7:
    print("Семиугольник")
elif side == 8:
    print("Восьмиугольник") 
elif side == 9:
    print("Девятиугольник") 
elif side == 10:
    print("Десятиугольник")
else:
    print("Предел диапазона")                        
